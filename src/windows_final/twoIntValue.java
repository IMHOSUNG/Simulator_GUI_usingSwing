package windows;

public class twoIntValue
{
    public int min;
    public int max;
    public int x;
    public int y;

    twoIntValue(int max, int min, int flag) {
        if(flag  == 0) {
            this.max = max;
            this.min = min;
        }
        else {
            this.x = x;
            this.y = y;
        }
    }
}

